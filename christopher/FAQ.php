        <p id="question1">1. Qu'est ce que Simplon(.co) ?</p>
        <p id="answer1">Simplon(.co) est une start-up basée à Montreuil (Seine Saint-Denis) dans le domaine de l'apprentissage de la programmation et du numérique. Fondée notemment par <a data-toggle="modal" data-target=".modalEK">Erwan Kezzar</a> et <a data-toggle="modal" data-target=".modalfb">Frédéric Bardeau</a> en 2013, la start-up permet à des personnes avec des profils peu courants dans la profession (sans diplômes, des personnes avec une déficience physique, des jeunes issus de quartiers défavorisés) de se former à la production de code informatique.</p>
        <p id="question2">2. Quelles sont les formations proposées ?</p>
        <p id="answer2">Plusieurs <a data-toggle="modal" data-target=".modalformation">formations</a> y sont dispensées dont celle en développement Web.</p>
        <p id="question3">3. Où est-implanté Simplon(.co) ?</p>
        <p id="answer3">Fort de son succès, Simplon est international : les centres de formation se trouvent en <a data-toggle="modal" data-target=".modalfrance">France + La réunion</a> mais également en Afrique du Sud, Roumanie, Côte d'Ivoire & au Liban !</p>
